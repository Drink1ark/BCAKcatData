def make_criterion(cfg: CFG) -> nn.Module:
    if cfg.loss == "smoothl1":
        return nn.SmoothL1Loss(beta=cfg.smoothl1_beta)
    if cfg.loss == "mse":
        return nn.MSELoss()
    if cfg.loss == "l1":
        return nn.L1Loss()
    raise ValueError("loss must be smoothl1, mse, or l1")


