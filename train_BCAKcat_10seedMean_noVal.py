def cosine_lr(optimizer: torch.optim.Optimizer, epoch: int, cfg: CFG) -> float:
    if epoch < cfg.warmup_epochs:
        lr = cfg.lr * (epoch + 1) / max(1, cfg.warmup_epochs)
    else:
        progress = (epoch - cfg.warmup_epochs) / max(1, (cfg.epochs - cfg.warmup_epochs))
        lr = cfg.min_lr + (cfg.lr - cfg.min_lr) * 0.5 * (1 + math.cos(math.pi * progress))
    for param_group in optimizer.param_groups:
        param_group["lr"] = lr
    return lr


def safe_corr(fn, y_true: np.ndarray, y_pred: np.ndarray) -> float:
    if len(y_true) <= 1:
        return 0.0
    if np.std(y_true) == 0 or np.std(y_pred) == 0:
        return 0.0
    try:
        val = float(fn(y_true, y_pred)[0])
    except Exception:
        return 0.0
    return val if np.isfinite(val) else 0.0


def calc_metrics(y_true: Iterable[float], y_pred: Iterable[float]) -> Dict[str, float]:
    y_true = np.asarray(y_true, dtype=np.float64).reshape(-1)
    y_pred = np.asarray(y_pred, dtype=np.float64).reshape(-1)
    mask = np.isfinite(y_true) & np.isfinite(y_pred)
    y_true = y_true[mask]
    y_pred = y_pred[mask]

    if len(y_true) == 0:
        return {"r2": -float("inf"), "pcc": 0.0, "spearman": 0.0, "rmse": float("inf"), "mae": float("inf")}

    if len(y_true) > 1 and np.std(y_true) > 0:
        r2 = float(r2_score(y_true, y_pred))
        if not np.isfinite(r2):
            r2 = -float("inf")
    else:
        r2 = 0.0

    rmse = float(np.sqrt(mean_squared_error(y_true, y_pred)))
    mae = float(mean_absolute_error(y_true, y_pred))
    return {
        "r2": r2,
        "pcc": safe_corr(pearsonr, y_true, y_pred),
        "spearman": safe_corr(spearmanr, y_true, y_pred),
        "rmse": rmse,
        "mae": mae,
    }


def safe_np_load(path: Path):
    try:
        return np.load(path, mmap_mode="r")
    except ValueError as e:
        print(f"[warn] mmap load failed for {path}: {e}")
        print(f"[warn] fallback to normal np.load for {path}")
        return np.load(path)


# ===================== Label scaling =====================
class LabelProcessor:
    def __init__(self, method: str = "standard"):
        self.method = method
        self.scaler = None

    def fit(self, labels: np.ndarray) -> None:
        labels = np.asarray(labels, dtype=np.float32).reshape(-1, 1)
        if self.method == "standard":
            self.scaler = StandardScaler().fit(labels)
        elif self.method == "robust":
            self.scaler = RobustScaler().fit(labels)
        elif self.method == "none":
            self.scaler = None
        else:
            raise ValueError(f"Unsupported label method: {self.method}")

    def transform(self, labels: np.ndarray) -> np.ndarray:
        labels = np.asarray(labels, dtype=np.float32).reshape(-1, 1)
        if self.scaler is None:
            return labels.flatten().astype(np.float32)
        return self.scaler.transform(labels).flatten().astype(np.float32)

    def inverse_transform(self, labels: np.ndarray) -> np.ndarray:
        labels = np.asarray(labels, dtype=np.float32).reshape(-1, 1)
        if self.scaler is None:
            return labels.flatten().astype(np.float32)
        return self.scaler.inverse_transform(labels).flatten().astype(np.float32)

    def state_dict(self) -> Dict[str, Any]:
        state: Dict[str, Any] = {"method": self.method}
        if self.scaler is None:
            state["scaler"] = None
        elif self.method == "standard":
            state["scaler"] = {
                "mean": self.scaler.mean_.tolist(),
                "scale": self.scaler.scale_.tolist(),
                "var": self.scaler.var_.tolist(),
                "n_samples_seen": int(getattr(self.scaler, "n_samples_seen_", 0)),
            }
        elif self.method == "robust":
            state["scaler"] = {
                "center": self.scaler.center_.tolist(),
                "scale": self.scaler.scale_.tolist(),
            }
        else:
            state["scaler"] = None
        return state

    def load_state_dict(self, state_dict: Dict[str, Any]) -> None:
        self.method = state_dict.get("method", self.method)
        scaler_state = state_dict.get("scaler")

        if scaler_state is None:
            self.scaler = None
        elif self.method == "standard":
            self.scaler = StandardScaler()
            self.scaler.mean_ = np.asarray(scaler_state["mean"], dtype=np.float64)
            self.scaler.scale_ = np.asarray(scaler_state["scale"], dtype=np.float64)
            self.scaler.var_ = np.asarray(scaler_state.get("var", np.ones_like(self.scaler.scale_)), dtype=np.float64)
            self.scaler.n_samples_seen_ = int(scaler_state.get("n_samples_seen", 0))
            self.scaler.n_features_in_ = int(len(self.scaler.mean_))
        elif self.method == "robust":
            self.scaler = RobustScaler()
            self.scaler.center_ = np.asarray(scaler_state["center"], dtype=np.float64)
            self.scaler.scale_ = np.asarray(scaler_state["scale"], dtype=np.float64)
            self.scaler.n_features_in_ = int(len(self.scaler.center_))
        else:
            self.scaler = None


# ===================== Dataset =====================
class TokenDataset(Dataset):
    def __init__(self, split_dir: str | Path, label_processor: Optional[LabelProcessor] = None, fit_label_processor: bool = False):
        self.split_dir = Path(split_dir)
        self.protein = safe_np_load(self.split_dir / "protein_tokens.npy")
        self.rxnfp = safe_np_load(self.split_dir / "rxnfp_tokens.npy")

        chemberta_path = self.split_dir / "chemberta_tokens.npy"
        fallback_path = self.split_dir / "molbert_tokens.npy"
        if chemberta_path.exists():
            self.chemberta = safe_np_load(chemberta_path)
            self.chemberta_source = "chemberta_tokens.npy"
        elif fallback_path.exists():
            self.chemberta = safe_np_load(fallback_path)
            self.chemberta_source = "molbert_tokens.npy"
        else:
            raise FileNotFoundError(f"Missing chemberta_tokens.npy or molbert_tokens.npy in {self.split_dir}")

        self.labels = safe_np_load(self.split_dir / "labels.npy")
        self.meta = pd.read_parquet(self.split_dir / "meta.parquet")

        lengths = [len(self.protein), len(self.rxnfp), len(self.chemberta), len(self.labels), len(self.meta)]
        if len(set(lengths)) != 1:
            raise ValueError(f"Length mismatch in {self.split_dir}: {lengths}")

        self.label_processor = label_processor or LabelProcessor()
        if fit_label_processor:
            self.label_processor.fit(self.labels)
        self.labels_proc = self.label_processor.transform(self.labels)

    def __len__(self) -> int:
        return len(self.labels)

    def __getitem__(self, idx: int):
        protein = torch.from_numpy(np.asarray(self.protein[idx]).copy()).float()
        rxnfp = torch.from_numpy(np.asarray(self.rxnfp[idx]).copy()).float()
        chemberta = torch.from_numpy(np.asarray(self.chemberta[idx]).copy()).float()
        label = torch.tensor(float(self.labels_proc[idx]), dtype=torch.float32)
        return protein, rxnfp, chemberta, label

    @property
    def feature_shapes(self) -> Dict[str, List[int]]:
        return {
            "protein": list(self.protein.shape),
            "rxnfp": list(self.rxnfp.shape),
            "chemberta": list(self.chemberta.shape),
            "labels": list(self.labels.shape),
            "chemberta_source": self.chemberta_source,
        }


# ===================== Overlap checks =====================
def exact_overlap_report(train_meta: pd.DataFrame, eval_meta: pd.DataFrame) -> Dict[str, Dict[str, Any]]:
    checks = {
        "sequence": ["sequence"],
        "reaction": ["reaction_smiles"],
        "sequence_reaction_pair": ["sequence", "reaction_smiles"],
    }
    report: Dict[str, Dict[str, Any]] = {}
    for name, cols in checks.items():
        missing = [c for c in cols if c not in train_meta.columns or c not in eval_meta.columns]
        if missing:
            report[name] = {"columns": cols, "skipped": True, "missing_columns": missing}
            continue
        train_set = set(map(tuple, train_meta[cols].astype(str).itertuples(index=False, name=None)))
        eval_set = set(map(tuple, eval_meta[cols].astype(str).itertuples(index=False, name=None)))
        overlap = train_set & eval_set
        report[name] = {
            "columns": cols,
            "train_unique": len(train_set),
            "eval_unique": len(eval_set),
            "overlap_count": len(overlap),
            "overlap_ratio_eval_unique": float(len(overlap) / max(1, len(eval_set))),
        }
    return report

# ===================== Rank Loss =====================
class RankLoss(nn.Module):
    """Pairwise ranking loss."""
    def __init__(self, margin: float = 1.0):
        super().__init__()
        self.margin = margin
 
    def forward(self, pred: torch.Tensor, target: torch.Tensor) -> torch.Tensor:
        # pred: [batch_size]
        # target: [batch_size]
        batch_size = pred.size(0)
        if batch_size < 2:
            return torch.tensor(0.0, device=pred.device)
 
        # Compute pairwise differences
        # pred_diff[i,j] = pred[i] - pred[j]
        pred_diff = pred.unsqueeze(1) - pred.unsqueeze(0)  # [batch, batch]
        target_diff = target.unsqueeze(1) - target.unsqueeze(0)  # [batch, batch]
 
        # Mask where target[i] > target[j]
        mask = (target_diff > 0).float()
        # If target[i] > target[j], we want pred[i] > pred[j]
        # Loss when pred[i] <= pred[j] when target[i] > target[j]
        loss = mask * torch.clamp(self.margin - pred_diff, min=0.0)
 
        # Sum over upper triangle (i > j) to avoid double counting
        upper_mask = torch.triu(torch.ones_like(mask), diagonal=1)
        loss = (loss * upper_mask).sum() / (upper_mask.sum() + 1e-6)
        return loss
